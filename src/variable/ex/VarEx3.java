package variable.ex;

public class VarEx3 {
    public static void main(String[] args) {
        long longVar = 10000000000L;
        System.out.println("longVar = " + longVar);  //soutv  하면 가장 가까운 변수를 출력해주는걸 자동으로 완성해줌 
        boolean booleanVar = true;
        System.out.println("booleanVar = " + booleanVar);
    }
}
